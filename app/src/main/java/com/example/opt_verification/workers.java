package com.example.opt_verification;

public class workers {

    String name , aadhar , work ;

    public workers() {

    }

    public workers(String name, String aadhar, String work) {
        this.name = name;
        this.aadhar = aadhar;
        this.work = work;
    }

    public String getName() {
        return name;
    }

    public String getAadhar() {
        return aadhar;
    }

    public String getWork() {
        return work;
    }
}
